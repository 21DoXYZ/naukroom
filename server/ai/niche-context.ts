function detectNiche(profile: Record<string, unknown>): NichePlaybook | null {
  const spec = ((profile.specialization ?? profile.profession ?? '') as string).toLowerCase()
  const currentServices = ((profile.currentServices ?? '') as string).toLowerCase()
  const combined = `${spec} ${currentServices}`

  if (combined.includes('після пологів') || combined.includes('постпологов') || combined.includes('мам')) {
    return PLAYBOOKS.find(p => p.niche === 'nutritionist_postpartum') ?? null
  }
  if (combined.includes('схуднен') || combined.includes('вага') || combined.includes('схудн')) {
    return PLAYBOOKS.find(p => p.niche === 'nutritionist_weightloss') ?? null
  }
  if (combined.includes('нутриціолог') || combined.includes('харчуван') || combined.includes('дієтолог')) {
    return PLAYBOOKS.find(p => p.niche === 'nutritionist_general') ?? null
  }
  if (combined.includes('health coach') || combined.includes('коуч') || combined.includes('велнес')) {
    return PLAYBOOKS.find(p => p.niche === 'health_coach') ?? null
  }
  if (combined.includes('косметолог') || combined.includes('шкір') || combined.includes('естетик')) {
    return PLAYBOOKS.find(p => p.niche === 'cosmetologist') ?? null
  }
  if (combined.includes('масаж') || combined.includes('тіло') || combined.includes('body')) {
    return PLAYBOOKS.find(p => p.niche === 'massage_body') ?? null
  }
  return PLAYBOOKS.find(p => p.niche === 'nutritionist_general') ?? null
}

export function buildNicheContext(profile: Record<string, unknown>): string {
  const playbook = detectNiche(profile)
  if (!playbook) return ''

  return `
## Контекст ніші (soft context — не шаблон, а орієнтир)
Визначена ніша: ${playbook.niche}
Типовий клієнт: ${playbook.typicalClient}
Головні болі: ${playbook.painPoints.join(', ')}
Бажані результати: ${playbook.desiredOutcomes.join(', ')}
Хороші офери для цієї ніші: ${playbook.bestOffers.join(', ')}
Хороші лід-магніти: ${playbook.bestLeadMagnets.join(', ')}
Теми контенту: ${playbook.contentThemes.join(', ')}
Уникати: ${playbook.avoid.join(', ')}

Якщо реальні дані користувача суперечать playbook — пріоритет завжди у реальних даних.`
}

